// Helper to find QING by address or name
window.findQing = function(addressOrName) {
  const search = (addressOrName || '').toLowerCase();
  return window.QING_DATABASE.find(q => 
    q.address.toLowerCase() === search || 
    q.name.toLowerCase().includes(search) ||
    search.includes(q.name.toLowerCase().replace(' qing', ''))
  );
};

// Helper to find QING by LAU name (adds " QING" suffix)
window.findQingByLauName = function(lauName) {
  if (!lauName) return null;
  const searchName = lauName.toLowerCase();
  return window.QING_DATABASE.find(q => {
    const qingBaseName = q.name.toLowerCase().replace(' qing', '');
    return qingBaseName === searchName || q.name.toLowerCase().includes(searchName);
  });
};

